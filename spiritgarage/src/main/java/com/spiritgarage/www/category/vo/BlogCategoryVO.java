package com.spiritgarage.www.category.vo;

public class BlogCategoryVO {

	private String blogCategorySeq;
	
	private String category;
	
	private String displayYn;
	
	private String regDate;
	
	private String uptDate;

	public String getBlogCategorySeq() {
		return blogCategorySeq;
	}

	public void setBlogCategorySeq(String blogCategorySeq) {
		this.blogCategorySeq = blogCategorySeq;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getDisplayYn() {
		return displayYn;
	}

	public void setDisplayYn(String displayYn) {
		this.displayYn = displayYn;
	}

	public String getRegDate() {
		return regDate;
	}

	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}

	public String getUptDate() {
		return uptDate;
	}

	public void setUptDate(String uptDate) {
		this.uptDate = uptDate;
	}
	
}
