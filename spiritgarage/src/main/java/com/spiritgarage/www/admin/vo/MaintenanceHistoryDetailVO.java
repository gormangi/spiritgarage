public class MaintenanceHistoryDetailVO {

	private String maintenanceHistoryDetailSeq;
	
	private String maintenanceHistorySeq;
	
	private String maintenanceDv;
	
	private String workContent;
	
	private String partsClass;
	
	private String regDate;

	public String getMaintenanceHistoryDetailSeq() {
		return maintenanceHistoryDetailSeq;
	}

	public void setMaintenanceHistoryDetailSeq(String maintenanceHistoryDetailSeq) {
		this.maintenanceHistoryDetailSeq = maintenanceHistoryDetailSeq;
	}

	public String getMaintenanceHistorySeq() {
		return maintenanceHistorySeq;
	}

	public void setMaintenanceHistorySeq(String maintenanceHistorySeq) {
		this.maintenanceHistorySeq = maintenanceHistorySeq;
	}

	public String getMaintenanceDv() {
		return maintenanceDv;
	}

	public void setMaintenanceDv(String maintenanceDv) {
		this.maintenanceDv = maintenanceDv;
	}

	public String getWorkContent() {
		return workContent;
	}

	public void setWorkContent(String workContent) {
		this.workContent = workContent;
	}

	public String getPartsClass() {
		return partsClass;
	}

	public void setPartsClass(String partsClass) {
		this.partsClass = partsClass;
	}

	public String getRegDate() {
		return regDate;
	}

	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	
}
